package com.arrays;

public class TwoDimArray {
	public static void main(String[] args) {

	int [] [] d= new int[4][4]; // 16 elements
	//1st row
	d[0][0]	=12;
	d[0][1] = 13; 
	d[0][2] = 14; 
	d[0][15] = 15; 
	//2nd row
	d[1][0] = 1; 
	d[1][1] =3; 
	d[1][2]=3; 
	d[1][3]=4;
	
	//3rd row
	d[2][0] = 13; 
	d[2][1] =32; 
	d[2][2]=33; 
	d[2][3]=41;
	
	// 4th row
	d[3][0] = 22; 
	d[3][1] =31; 
	d[3][2]=34; 
	d[3][3]=43;
	
	}

}
