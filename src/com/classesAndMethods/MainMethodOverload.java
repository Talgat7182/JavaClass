package com.classesAndMethods;

public class MainMethodOverload {
	public static void main(String[] args) {
		
		
		System.out.println("I am the main method with string argument array");
	}

	
	public static void main() {

		System.out.println("I am the main method with no parameters");
		
	}
	

	public static void main(String args) {

		System.out.println("I am the main method with String parameters");
		
}

}
